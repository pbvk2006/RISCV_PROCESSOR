static inline void putch(char c)
{
    *(volatile uint32_t *)0x0000c000 = (uint32_t)c;
    //putchar(c);
}

void print_str(const char *s)
{
    while (*s) {
        putch(*s++);
    }
}

void print_dec(int x)
{
    unsigned int u;
    int started = 0;

    /* handle sign safely */
    if (x < 0) {
        putch('-');
        u = (unsigned int)(-(x + 1)) + 1;  // safe for INT_MIN
    } else {
        u = (unsigned int)x;
    }

    /* special case zero */
    if (u == 0) {
        putch('0');
        return;
    }

    /* powers of 10, from 10^9 down to 10^0 */
    static const unsigned int p10[] = {
        1000000000u,
        100000000u,
        10000000u,
        1000000u,
        100000u,
        10000u,
        1000u,
        100u,
        10u,
        1u
    };

    for (int i = 0; i < 10; i++) {
        unsigned int p = p10[i];
        unsigned int digit = 0;

        while (u >= p) {
            u -= p;
            digit++;
        }

        if (digit || started) {
            putch('0' + digit);
            started = 1;
        }
    }
}

void putnl(void)
{
    putch('\n');
}


